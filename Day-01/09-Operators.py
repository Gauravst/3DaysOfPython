# Python Operators
x = 10
y = 2

# Arithmetic Operaters
print("x + y is :", x+y) # 12
print("x - y is :", x-y) # 8
print("x * y is :", x*y) # 20
print("x / y is :", x/y) # 5.0
print("x % y is :", x%y) # 0
print("x ** y is :", x**y) # 100 
print("x // y is :", x//y) # 5

